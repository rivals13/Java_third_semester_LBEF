class RelationalExample{
	public static void main(String...  args){ //  strings..  refers to the variable  string  object 
		double a=10.0;
		int b=3;
		System.out.println("a>b:"+(a>b));
		System.out.println("a>=b:"+(a>=b));
		System.out.println("a<b:"+(a<b));
		System.out.println("a<=b:"+(a<=b));
		System.out.println("a==b:"+(a==b));
		System.out.println("a!=b:"+(a!=b));
		//  the true  false   are  in  smaller case
	}
}

